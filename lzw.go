package main

import (
	"bytes"
	"encoding/base64"
	"encoding/binary"
	"fmt"
	"io"
	"os"
)

/*
1. 16-bit fixed width codes
2. The dictionary is initialised with 256 entries, which map all the possible values for a byte to entries of length 1 containing that value:
	0 -> \x00
	1 -> \x01
	65 -> "A"
	66 -> "B"
3. When all the possible codes have been used, the dictionary is reset to the initial dictionary described in 2.
*/

func decode(codes []uint16) string {
	dict := make(map[uint16]string)
	var next uint16
	for next = 0; next < 256; next++ {
		dict[next] = string(rune(next))
	}

	prev := ""
	result := ""

	for i := 0; i < len(codes); i++ {
		code := codes[i]

		if code < next {
			ret := dict[code]
			result += ret
			if prev == "" {
				prev = ret
				continue
			} else {
				dict[next] = prev + string(ret[0])
				prev = ret
			}
		} else {
			ret := prev + string(prev[0])
			result += ret
			dict[next] = ret
			prev = ret
		}
		next++
	}

	return result
}

func read(in io.Reader) []uint16 {
	codes := []uint16{}

	for {
		b := make([]byte, 2)
		n, _ := io.ReadFull(in, b)
		if n != 2 {
			return codes
		}
		codes = append(codes, binary.LittleEndian.Uint16(b))
	}
}

func main() {
	for name, text := range testData {
		fmt.Printf("%s:\n", name)

		input, err := base64.StdEncoding.DecodeString(text)

		// catch and print error
		fmt.Println("error =", err)

		codes := read(bytes.NewReader(input))

		data := decode(codes)

		os.Stdout.WriteString(data)

		fmt.Println("")
	}
}

